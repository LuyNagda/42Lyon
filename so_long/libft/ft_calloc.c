#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	char	*p;

	if (nmemb * size == 0)
		return (malloc(0));
	if (nmemb * size > 2147483647)
		return (NULL);
	if (nmemb > 65535 || size > 65535)
		return (NULL);
	p = malloc(nmemb * size);
	if (!p)
		return (NULL);
	ft_bzero(p, nmemb * size);
	return (p);
}
